void main() {
  // +, -, *, /, %, ++, +=, -=, *=, /=,
  //--, >, <, >=, <=, ==, &&
  print(45 + 45);
  print('sdfsdf' + 'po sdf'); // String concat
  print(50-20);
  print(50*4);
  print(50/4);
  print(10%3);
  print(10 == 20);
  int a = 1;
  print(a);
  a += 1; // a = a+1;
  a++; // a = a+1;
  print(a);

  a -= 1;
  print(a);
  a--;
  print(a);
  a += 7;
  print(a);
  print(a++);
  print(++a + 23);
  // print(a++ + 23);
  print(a == 34); // equal to
  print(a != 34); // not equal to

  print(a > 45);
  print(a < 45);
  print(a);
  print(a >= 35);
  print(10 <= 35);
}